	public static void main(String[] args) {
		label();
		Customer customer =new Customer();
		Menu menu = new Menu();
		

		Scanner sn = new Scanner(System.in);

		String selected = "";
		do {

			System.out.println("-----------------------------------------------------");
			System.out.println("              Maid Cafe' System                      ");
			System.out.println("-----------------------------------------------------");
			System.out.println("Customer Register Select \t\t\t(1)");
			System.out.println("Customer List Select \t\t\t\t(2)");
			System.out.println("Menu Request Order Select \t\t\t(3)");
			System.out.println("Out Order Select \t\t\t\t(4)");
			System.out.println("ListOrderWait \t\t\t\t\t(5)");
			System.out.println("---------------------");
			System.out.print("Please Select > ");
			selected = sn.next();
			
			
			switch(selected) {
			case "1":
				customer.Register();
				break;
			case "2":
				customer.ListCustomer();
				break;
			case "3":
				if(customer.getCustomers().size() !=0) {
					menu.RequestOrder(customer);
				}else {
					System.out.println("please add customer");
				}
				break;
			case "4":
				menu.OutOrder();
				break;
			case "5":
				menu.ListOrderWait();
				break;
			}
			
			System.out.println("Exit Select (0)");
		}while(!selected.equals("0"));
		
	}
